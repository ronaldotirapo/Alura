/*Existen distintas formas de declarar variables en JavaScript,
aunque se puede declarar variables sin las palabras reservadas es mejor hacerlas según se necesiten:
const, para constantes
var, para variables con acceso global
let, para variables con acceso local
Además los nombres de variables no deben empezar en mayúscula o número ni incluir o empezar con carácteres especiales
(exepto _ ) y para usar varias palabras se continua con mayúscula (Camel case)*/
const nombre = "Ronaldo Tirapo";
let direccion = "Av. Caraz 275";
var costoPasaje = 1500;
let tasaEmabrque = 50.5;

/*Existen distintas convenciones para establecer el nombre de las variables:
camelCase, se escribe en minúscular y para continuar la palabra se usa mayúscula (Recomendado para variables)
snake_case, se escribe en minúscular y para continuar la palabra se usa _ (Recomendado para nombres de archivo)
SCREAM_CASE, similar a snake case pero se escribe en mayúsculas (Recomendado para const)

kebab-case, se escribe en minúscular y para continuar la palabra se usa _ (no se puede usar para nombrar variables pero sí archivos)*/
